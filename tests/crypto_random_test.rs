#[test]
fn test_generate_random_bool() {
    let mut true_count = 0;
    let mut false_count = 0;
    
    // Generate many booleans and check distribution
    for _ in 0..1000 {
        let result = generate_random_bool();
        assert!(result.is_ok(), "generate_random_bool should succeed");
        
        if result.unwrap() {
            true_count += 1;
        } else {
            false_count += 1;
        }
    }
    
    // Both true and false should appear (very high probability)
    assert!(true_count > 0, "Should generate some true values");
    assert!(false_count > 0, "Should generate some false values");
    
    // Distribution should be roughly even (allowing for variance)
    let ratio = true_count as f64 / 1000.0;
    assert!(ratio > 0.3 && ratio < 0.7, "Boolean distribution should be roughly even");
}

