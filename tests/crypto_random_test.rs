#[test]
fn test_thread_safety() {
    let num_threads = 8;
    let operations_per_thread = 100;
    
    let handles: Vec<_> = (0..num_threads)
        .map(|_| {
            thread::spawn(move || {
                for _ in 0..operations_per_thread {
                    // Test various operations concurrently
                    let _bytes = generate_random_bytes(32).unwrap();
                    let _u32_val = generate_random_u32().unwrap();
                    let _bool_val = generate_random_bool().unwrap();
                }
            })
        })
        .collect();
    
    // Wait for all threads to complete
    for handle in handles {
        handle.join().expect("Thread should complete successfully");
    }
    
    // Verify statistics show all operations
    let stats = get_rng_statistics().unwrap();
    let expected_operations = num_threads * operations_per_thread * 3; // 3 ops per iteration
    assert!(stats.generation_count >= expected_operations as u64, 
           "Should record all concurrent operations");
}

#[test]
fn test_randomness_distribution() {
    const SAMPLE_SIZE: usize = 10000;
    let data = generate_random_bytes(SAMPLE_SIZE).unwrap();
    
    // Count frequency of each byte value
    let mut counts = [0u32; 256];
    for &byte in &data {
        counts[byte as usize] += 1;
    }
    
    // Calculate chi-square statistic for uniformity test
    let expected = SAMPLE_SIZE as f64 / 256.0;
    let mut chi_square = 0.0;
    
    for &count in &counts {
        let diff = count as f64 - expected;
        chi_square += (diff * diff) / expected;
    }
    
    // For 255 degrees of freedom, critical value at 0.05 significance is ~293.25
    // We use a more lenient threshold since this is a basic test
    assert!(chi_square < 350.0, "Chi-square test suggests good distribution: {}", chi_square);
}

