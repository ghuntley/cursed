#[tokio::test]
async fn test_validation_without_package_integration() {
    // Test that compilation works without package integration
    let mut codegen = LlvmCodeGenerator::new().unwrap();
    
    let source = r#"
slay main() {
    capicola("Hello without packages!");
}
"#;
    
    // Should fall back to regular compilation
    let result = codegen.compile_with_packages(source, None).await;
    assert!(result.is_ok());
    
    let ir = result.unwrap();
    assert!(ir.contains("Generated by CURSED Compiler"));
    
    // Package methods should return default values
    assert!(!codegen.package_integration_enabled());
    assert!(!codegen.has_package_symbol("any_symbol"));
    assert!(codegen.get_package_symbols().is_empty());
    assert!(codegen.get_package_stats().is_none());
}

