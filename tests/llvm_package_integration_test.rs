/// Integration tests for LLVM package system
/// 
/// Tests the integration between the package manager and LLVM compilation pipeline.

use cursed::codegen::llvm::{LlvmCodeGenerator, LlvmPackageConfig, LlvmPackageIntegration};
use cursed::package_manager::{PackageManager, PackageManagerConfig};
use std::sync::{Arc, Mutex};

#[tokio::test]
async fn test_llvm_package_integration_initialization() {
    // Create package manager
    let package_manager_config = PackageManagerConfig::default();
    let package_manager = Arc::new(Mutex::new(
        PackageManager::new(package_manager_config).unwrap()
    ));
    
    // Create LLVM code generator
    let mut codegen = LlvmCodeGenerator::new().unwrap();
    
    // Initialize package integration
    let package_config = LlvmPackageConfig::default();
    let result = codegen.initialize_package_integration(package_manager, package_config);
    
    assert!(result.is_ok());
    assert!(codegen.package_integration_enabled());
}

#[tokio::test]
async fn test_simple_compilation_with_packages() {
    // Create package manager
    let package_manager_config = PackageManagerConfig::default();
    let package_manager = Arc::new(Mutex::new(
        PackageManager::new(package_manager_config).unwrap()
    ));
    
    // Create LLVM code generator with package integration
    let mut codegen = LlvmCodeGenerator::new().unwrap();
    let package_config = LlvmPackageConfig {
        auto_install_packages: false, // Disable auto-install for test
        ..LlvmPackageConfig::default()
    };
    
    codegen.initialize_package_integration(package_manager, package_config).unwrap();
    
    // Simple CURSED source without external packages
    let source = r#"
slay main() {
    capicola("Hello, World!");
}
"#;
    
    let result = codegen.compile_with_packages(source, None).await;
    assert!(result.is_ok());
    
    let ir = result.unwrap();
    assert!(ir.contains("Generated by CURSED Compiler with Package Integration"));
    assert!(ir.contains("target datalayout"));
    assert!(ir.contains("target triple"));
}

