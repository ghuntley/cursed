#[cfg(test)]
mod helper_tests {
    use super::*;
    use cursed::ast::ImportStatement;
    
    #[test]
    fn test_import_validation() {
        let mut codegen = LlvmCodeGenerator::new().unwrap();
        
        // Valid imports
        let valid_imports = vec![
            ImportStatement {
                token: cursed::lexer::Token::new(cursed::lexer::TokenType::Identifier, "stdlib::io"),
                path: "stdlib::io".to_string(),
                alias: None,
            },
            ImportStatement {
                token: cursed::lexer::Token::new(cursed::lexer::TokenType::Identifier, "package::module"),
                path: "package::module".to_string(),
                alias: None,
            },
        ];
        
        let result = codegen.validate_package_dependencies(&valid_imports);
        assert!(result.is_ok());
        
        // Invalid imports
        let invalid_imports = vec![
            ImportStatement {
                token: cursed::lexer::Token::new(cursed::lexer::TokenType::Identifier, ""),
                path: "".to_string(), // Empty path
                alias: None,
            },
        ];
        
        let result = codegen.validate_package_dependencies(&invalid_imports);
        assert!(result.is_err());
    }
}
