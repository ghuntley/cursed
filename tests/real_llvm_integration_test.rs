#[test]
fn test_optimization_integration() {
    common::tracing::setup();
    
    let mut generator = LlvmCodeGenerator::new().expect("Failed to create generator");
    
    // Verify optimization is enabled by default
    assert!(generator.optimization_enabled(), "Optimization should be enabled by default");
    
    // Test disabling optimization
    generator.disable_optimization();
    assert!(!generator.optimization_enabled(), "Optimization should be disabled");
    
    // Test re-enabling optimization
    let optimization_config = cursed::optimization::OptimizationConfig::default();
    let enable_result = generator.enable_optimization(optimization_config);
    assert!(enable_result.is_ok(), "Failed to enable optimization: {:?}", enable_result.err());
    assert!(generator.optimization_enabled(), "Optimization should be enabled again");
}

#[test]
fn test_runtime_integration() {
    common::tracing::setup();
    
    // Create generator with custom runtime
    let runtime = Runtime::new().expect("Failed to create runtime");
    let runtime_arc = Arc::new(runtime);
    
    let generator_result = LlvmCodeGenerator::new_with_runtime(runtime_arc.clone());
    assert!(generator_result.is_ok(), "Failed to create generator with runtime");
    
    let generator = generator_result.unwrap();
    
    // Verify runtime integration
    let generator_runtime = generator.get_runtime();
    assert!(Arc::ptr_eq(&runtime_arc, &generator_runtime), "Runtime should be the same instance");
}
