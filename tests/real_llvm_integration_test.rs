#[test]
fn test_llvm_type_conversion() {
    common::tracing::setup();
    
    let generator = LlvmCodeGenerator::new().expect("Failed to create generator");
    
    // Test various CURSED type to LLVM type conversions
    let test_cases = vec![
        ("normie", "i32"),
        ("sus", "i32"),
        ("i64", "i64"), 
        ("facts", "i1"),
        ("bool", "i1"),
        ("vibes", "f64"),
        ("f64", "f64"),
        ("double", "f64"),
        ("tea", "i8*"),
        ("string", "i8*"),
    ];
    
    for (cursed_type, expected_llvm) in test_cases {
        let result = generator.cursed_type_to_llvm_type(cursed_type);
        assert!(result.is_ok(), "Failed to convert type {}: {:?}", cursed_type, result.err());
        
        let llvm_type = result.unwrap();
        
        // Verify the type is correct (simplified check)
        match expected_llvm {
            "i32" => assert!(llvm_type.is_int_type(), "Expected i32 type for {}", cursed_type),
            "i64" => assert!(llvm_type.is_int_type(), "Expected i64 type for {}", cursed_type),
            "i1" => assert!(llvm_type.is_int_type(), "Expected i1 type for {}", cursed_type),
            "f64" => assert!(llvm_type.is_float_type(), "Expected f64 type for {}", cursed_type),
            "i8*" => assert!(llvm_type.is_pointer_type(), "Expected pointer type for {}", cursed_type),
            _ => panic!("Unknown expected type: {}", expected_llvm),
        }
    }
}

